public class LacosCondicionais{
	public static void main(String[] args){
		int contador = 0;

		/*
		executa bloco de instrução, enquanto a instrução for verdadeira
		estrutura de repetição, iteração, looping
		*/
		while(contador <= 9){
			contador = contador + 1;
			System.out.println("looping");
		}
	}
}